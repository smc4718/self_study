package variable;

public class Var5 {

    public static void main(String[] args) {
        int a;  // 선언
        a = 1;  // 초기화
        System.out.println(a);  // 출력

        int b = 2; // 변수 선언과 초기화를 한 번에.
        System.out.println(b);

        int c = 3, d= 4; // 여러 변수 선언과 초기화를 한 번에.
        System.out.println(c);
        System.out.println(d);

    }
}
